class Solution{
    public:
    int countSetBits(int n)
    {
        if(n<=0)
           return 0;
        
        int p=log2(n);
        
        return p*(pow(2,p)/2)+(n-pow(2,p)+1)+countSetBits(n-pow(2,p));
    }
};


