Node* solve(Node* root,Node* node)
{
    Node* temp=new Node(0);
    
    Node* x=temp;
    
    while(root && node)
    {
        if(root->data<node->data)
        {
            x->bottom=root;
            x=x->bottom;
            root=root->bottom;
        }
        else
        {
            x->bottom=node;
            x=x->bottom;
            node=node->bottom;
        }
    }
    
    while(root)
    {
        x->bottom=root;
        root=root->bottom;
        x=x->bottom;
    }
    
    while(node)
    {
        x->bottom=node;
        node=node->bottom;
        x=x->bottom;
    }
    
    return temp->bottom;
}

Node *flatten(Node *root)
{
   Node* node;
   if(root->next!=NULL)
      node=flatten(root->next);
   else
      return root;
   return solve(root,node);
}

